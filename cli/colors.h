/* colors.h - RGB color constants for the REPL
 *
 * All colors are from the CharmTone palette
 * (github.com/charmbracelet/x/exp/charmtone).
 */

#ifndef DITTY_BIN_COLORS_H
#define DITTY_BIN_COLORS_H

/* Prompt ">>>" — Flamingo (#F947E3) */
#define COLOR_PROMPT_R 0xf9
#define COLOR_PROMPT_G 0x47
#define COLOR_PROMPT_B 0xe3

/* Divider — Oyster (#605F6B) */
#define COLOR_DIVIDER_R 0x60
#define COLOR_DIVIDER_G 0x5f
#define COLOR_DIVIDER_B 0x6b

/* Error messages — Coral (#FF577D) */
#define COLOR_ERROR_R 0xff
#define COLOR_ERROR_G 0x57
#define COLOR_ERROR_B 0x7d

/* Welcome/info text — Oyster (#605F6B) */
#define COLOR_INFO_R 0x60
#define COLOR_INFO_G 0x5f
#define COLOR_INFO_B 0x6b

/* nil — Oyster (#605F6B) */
#define COLOR_NIL_R 0x60
#define COLOR_NIL_G 0x5f
#define COLOR_NIL_B 0x6b

/* Numbers — Sardine (#4FBEFE) */
#define COLOR_NUMBER_R 0x4f
#define COLOR_NUMBER_G 0xbe
#define COLOR_NUMBER_B 0xfe

/* Strings — Julep (#00FFB2) */
#define COLOR_STRING_R 0x00
#define COLOR_STRING_G 0xff
#define COLOR_STRING_B 0xb2

/* Symbols — Mustard (#F5EF34) */
#define COLOR_SYMBOL_R 0xf5
#define COLOR_SYMBOL_G 0xef
#define COLOR_SYMBOL_B 0x34

/* Functions/macros — Hazy (#8B75FF) */
#define COLOR_FUNCTION_R 0x8b
#define COLOR_FUNCTION_G 0x75
#define COLOR_FUNCTION_B 0xff

/* Lists/vectors/tables — Steep (#D6D3DC) */
#define COLOR_RESULT_R 0xd6
#define COLOR_RESULT_G 0xd3
#define COLOR_RESULT_B 0xdc

#endif /* DITTY_BIN_COLORS_H */
